void readStringUntil(UART_HandleTypeDef* huart, char terminator, char* buffer, uint16_t maxBufferSize) {
	uint8_t receivedChar;
	uint16_t index = 0;

	do {
		HAL_UART_Receive(huart, &receivedChar, 1, HAL_MAX_DELAY);

		if (index < maxBufferSize - 1) {
			buffer[index++] = receivedChar;
		} else {
			// Handle buffer overflow error
			break;
		}
	} while (receivedChar != terminator && receivedChar != '\r');  // '\r' is used as an additional terminator for CR/LF line endings

	buffer[index] = '\0'; // Null-terminate the string
}

